package model;

import java.util.ArrayList;

public class Fournisseur {
    private int idF;
    private String nomF;
    private String prenomF;
    private String adresseF;
    private String villeF;

    public int getIdF() {
        return idF;
    }

    public void setIdF(int idF) {
        this.idF = idF;
    }

    public String getNomF() {
        return nomF;
    }

    public void setNomF(String nomF) {
        this.nomF = nomF;
    }

    public String getPrenomF() {
        return prenomF;
    }

    public void setPrenomF(String prenomF) {
        this.prenomF = prenomF;
    }

    public String getAdresseF() {
        return adresseF;
    }

    public void setAdresseF(String adresseF) {
        this.adresseF = adresseF;
    }

    public String getVilleF() {
        return villeF;
    }

    public void setVilleF(String villeF) {
        this.villeF = villeF;
    }
}
